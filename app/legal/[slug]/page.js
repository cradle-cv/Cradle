export async function generateMetadata({ params }) {
  const { slug } = await params
  const doc = DOCS[slug]
  if (!doc) return { title: '法律与声明' }
  return {
    title: doc.title,
    description: `摇篮（cradle.art）${doc.title}`,
    alternates: { canonical: `/legal/${slug}` },
  }
}

// 轻量渲染:## 小节 / **加粗行** / - 列表 / 空行分段
function renderBody(body) {
  const blocks = []
  let list = []
  const flushList = (key) => {
    if (list.length) {
      blocks.push(
        <ul key={`ul-${key}`} style={{ margin: '0 0 18px', paddingLeft: '22px' }}>
          {list.map((li, i) => (
            <li key={i} style={{ fontSize: '15.5px', lineHeight: 2, color: '#1F2937', marginBottom: '6px' }}>{renderInline(li)}</li>
          ))}
        </ul>
      )
      list = []
    }
  }
  const renderInline = (text) => {
    const parts = text.split(/\*\*(.+?)\*\*/g)
    return parts.map((p, i) => (i % 2 === 1 ? <strong key={i}>{p}</strong> : p))
  }
  body.split('\n').forEach((raw, idx) => {
    const line = raw.trim()
    if (!line) { flushList(idx); return }
    if (line.startsWith('## ')) {
      flushList(idx)
      blocks.push(
        <h2 key={idx} style={{ fontSize: '17px', fontWeight: 700, color: '#111827', margin: '30px 0 12px' }}>
          {line.slice(3)}
        </h2>
      )
    } else if (line.startsWith('- ')) {
      list.push(line.slice(2))
    } else {
      flushList(idx)
      blocks.push(
        <p key={idx} style={{ fontSize: '15.5px', lineHeight: 2.05, color: '#1F2937', margin: '0 0 16px', textAlign: 'justify' }}>
          {renderInline(line)}
        </p>
      )
    }
  })
  flushList('end')
  return blocks
}

